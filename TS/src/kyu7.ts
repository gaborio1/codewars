// Example: (input --> output)

// "ATTGC" --> "TAACG"
// "GTAT" --> "CATA"
// dnaStrand []        `shouldBe` []
// dnaStrand [A,T,G,C] `shouldBe` [T,A,C,G]
// dnaStrand [G,T,A,T] `shouldBe` [C,A,T,A]
// dnaStrand [A,A,A,A] `shouldBe` [T,T,T,T]

const dnaStrand = (dna: string): string => {

    const arr = dna.split("");


    arr.map((el, i) => {

        switch (el) {
            case "A":
                arr[i] = "T"
                break
            case "T":
                arr[i] = "A"
                break
            case "C":
                arr[i] = "G"
                break
            case "G":
                arr[i] = "C"
        }
    });

    console.log(arr.join(""));
    return arr.join("");
}

