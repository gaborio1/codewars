// 1️⃣    ARRAY OF FIRST n ELEMENTS
const fibonacciFirstN = (num: number): number[] => {

    const fibSequence: number[] = [0, 1];

    if (num === 1) return [fibSequence[0]];
    if (num === 2) return fibSequence;

    for (let i = 0; i < num - 2; i++) {
        let next = fibSequence[i] + fibSequence[i + 1];
        fibSequence.push(next);

    }

    return fibSequence;

}

console.log(fibonacciFirstN(5));

// 2️⃣    nTH ELEMENT
const fibonacciNth = (num: number): number => {

    const fibSequence: number[] = [0, 1];

    if (num === 1) return fibSequence[0];
    if (num === 2) return fibSequence[1];

    for (let i = 0; i < num - 2; i++) {
        let next = fibSequence[i] + fibSequence[i + 1];
        fibSequence.push(next);

    }

    return fibSequence[fibSequence.length - 1];

}

console.log(fibonacciNth(5));

