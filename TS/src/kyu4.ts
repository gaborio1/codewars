function nextBigger6(n: number): number {
    const originalArr = ("" + n).split("").sort().join("");
    let newArr = "";
    let current = n + 1;
    if (
        n.toString().split("").join("") ==
        [...n.toString().split("")].sort().reverse().join("")
    )
        return -1;
    while (originalArr !== newArr) {
        newArr = ("" + current).split("").sort().join("");
        current++;
    }
    return current - 1;
}

