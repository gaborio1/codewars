// RETURN INTEGER WITH DESCENDING DIGITS
// 1/3/20

function descendingOrder(num) {
    var splitDigitArr = (num + '').split('').map((i) => { return Number(i); }); // SPLIT NUM TO INDIVIDUAL DIGITS
    var descendingArr = splitDigitArr.sort(function(a, b){return b-a});  //  SORT ARRAY IN DESCENDING ORDER    
    var  joinedArr = descendingArr.join(""); //  JOIN ARRAY OF NUMS INTO ONE CONCATENATED NUMERIC STRING
    return Number(joinedArr); // MAKE STRING A NUMBER
  }
  
