//  FUNCTION FINDSHORT - RETURN LENGTH OF SHORTEST WORD IN STRING
//  29/2/20

function findShort(str) {
    var str = str;
    var words = str.split(' '); // CONVERT STR TO ARRAY OF STR'S
    var result = words[0].length; // SET RESULT EQUAL TO FIRST WORD'S LENGTH
    for(var i = 0; i < words.length; i++) {
      if(words[i].length < result) {  // IF AN ELEMENT IS SHORTER THAN RESULT:
        result = words[i].length; //  THEN UPDATE RESULT
      }
    }
    return result;
}
  
