// TO JADEN CASE - UPPER CASE EVERY WORD IN STRING
// 4/3/20

String.prototype.toJadenCase = function() {
    var words = this.toLowerCase().split(" ");     // CONVERT STR INTO LOWERCASE ARRAY OF LETTERS - WITH SEPARATOR ' ' 1 SPACE=BETWEEN EACH WORD
    var result = [];                              // DECLARE EMPTY ARRAY FOR RESULT
    for (var i = 0; i < words.length; i++) {
      result.push(words[i]);                      // PUSH EACH WORD INTO ARRAY
    }
    result = result
      .map(str => str.replace(/(^|\s)[a-z]/g, letter => letter.toUpperCase()))     //  REGEX REGULAR EXPRESSION /(^|\s)[a-z]/g 
      .join(" ");                                                                  //  
    return result;                                                                 //  
}                               
  
