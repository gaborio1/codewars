function getCount(str) {
    var vowelsCount = 0;
    // var vowels = ["a", "e", "i", "o", "u"];
    for (var i = 0; i < str.length; i++) {
      if (
        str.charAt(i) === "a" ||
        str.charAt(i) === "e" ||
        str.charAt(i) === "i" ||
        str.charAt(i) === "o" ||
        str.charAt(i) === "u"
      ) {
        vowelsCount += 1;
      }
    }
    return vowelsCount;
}

