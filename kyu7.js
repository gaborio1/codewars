// REFACTOR USING OBJECT var pairs = {'A':'T','T':'A','C':'G','G':'C'};

function DNAStrand(str){
    let strArr = str.split("");                     //  SPLIT CHARACTERS INTO ARRAY
    for (let i = 0 ; i<strArr.length; i++) {        //  LOOP THROUGH ARRAY AND CHANGE LETTER IF CONDITON IS MET
      if (strArr[i] === "A") {
        strArr[i] = "T";
      } else if  (strArr[i] === "T") {
        strArr[i] = "A";
      } else if  (strArr[i] === "C") {
        strArr[i] = "G";
      } else if  (strArr[i] === "G") {
        strArr[i] = "C";
      }
    }
    return strArr.join("");           //  JOIN CHARS INTO CONCENATED WORDS
}
  
  
